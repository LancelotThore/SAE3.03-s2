function handlerClick(ev) {
    if (ev.target.id == 'group') {
        let result;
        if (ev.target.value == "tout") {
            result = all;
        }
        else {
            result = M.filterByTag("group", ev.target.value);
        }
        renderTimes(result);
    }

    if (ev.target.id == 'week') {
        let result;
        result = M.filterByTag("week", ev.target.value);
        getDernierCours(result);
    }
}

